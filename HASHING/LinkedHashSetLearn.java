import java.util.LinkedHashSet;

public class LinkedHashSetLearn {
  public static void main(String[] args) {
   LinkedHashSet<String> lhs = new LinkedHashSet<>(); //creating hashset 

   lhs.add("Mumbai");
   lhs.add("Delhi");
   lhs.add("Chennai");
   lhs.add("Kolkata");
   lhs.add("Pune");
   
   System.out.println(lhs);
  }
}
